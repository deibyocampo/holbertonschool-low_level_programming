#include "3-calc.h"
/**
 * op_add - sum a and b.
 *
 * @a: integer 1.
 * @b: integer 2.
 *
 * Return: result the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction a and b
 *
 * @a: integer 1.
 * @b: integer 2.
 *
 * Return: result the subtraction.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication a and b.
 *
 * @a: integer 1.
 * @b: integer 2.
 *
 * Return: result the multiplication.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division a and b.
 *
 * @a: integer 1.
 * @b: integer 2.
 *
 * Return: result the division.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - module a and b.
 *
 * @a: integer 1.
 * @b: integer 2.
 *
 * Return: result the module.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
